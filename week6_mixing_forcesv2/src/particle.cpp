//------------------------------------------------------------
void particle::addDampingForce(){
	
	// the usual way to write this is  vel *= 0.99
	// basically, subtract some part of the velocity 
	// damping is a force operating in the oposite direction of the 
	// velocity vector
	
    frc.x = frc.x - vel.x * damping;
    frc.y = frc.y - vel.y * damping;
}

//------------------------------------------------------------
void particle::setInitialCondition(float px, float py, float vx, float vy){
    pos.set(px,py);
	vel.set(vx,vy);
    
    //drawing variables
    float angle = atan2(pos.y-ofGetHeight()/2,pos.x-ofGetWidth()/2)+PI;
    float distance = ofDist(pos.x,pos.y,ofGetWidth()/2,ofGetHeight()/2);
    float hue = angle/TWO_PI*255;
    float sat = ofMap(distance,0,ofGetWidth()/4,150,200,true);
    float brightness = ofMap(distance,ofGetWidth()/2,ofGetWidth()/2,180,100,true);
    color = ofColor::fromHsb(hue,sat,brightness);
}

//------------------------------------------------------------
void particle::update(){	
	vel = vel + frc;
	pos = pos + vel;
    
    
    if(trail.size()>100);{
        trail.push_back(pos);
    }
}

//------------------------------------------------------------
void particle::draw(){    
    
    ofSetColor(200,200);
	ofPoint temp;
	temp.x = pos.x;
	temp.y = pos.y;
	trail.push_back(temp);
	if (trail.size() > 100){
		trail.erase(trail.begin());
	}
    
	ofNoFill();
    ofSetColor(color,255);
    //ofMesh lineMesh;
	ofBeginShape();
    ofSetLineWidth(1);
	for (int i = 0; i < trail.size(); i++){
        //lineMesh.addVertex(trail[i]);
		ofVertex(trail[i].x, trail[i].y);
	}
	ofEndShape();
    
    float dx = pos.x - prevPos.x;
    float dy = pos.y - prevPos.y;
    
    angle = atan2(dy, dx)+ PI;
    
    prevPos.x = pos.x;
    prevPos.y = pos.y;
    
    //the lead
    ofFill();
    ofSetCircleResolution(lead);
    ofPushMatrix();
    ofTranslate(pos.x, pos.y);
    ofRotateZ(angle * RAD_TO_DEG);
    ofCircle(0, 0, size);
    ofPopMatrix();
    
    ofPushMatrix();
//    ofBeginShape();
//	for (int i = 0; i < trail.size(); i++){
//        //lineMesh.addVertex(trail[i]);
//		ofVertex(trail[i].x, trail[i].y);
//	}
//    
    ofSetCircleResolution(lead);
	ofEndShape();
    ofTranslate(pos.x, pos.y);
    ofRotateZ(angle * RAD_TO_DEG);
    ofEllipse(40,40,50,size);
    ofPopMatrix();
    
    
    ofPushMatrix();
    ofSetColor(color,55);
    ofSetCircleResolution(lead);
	ofEndShape();
    ofTranslate(ofGetMouseX(),ofGetMouseY());
    ofRotateZ(angle * RAD_TO_DEG);
    ofEllipse(20,20,50,size);
    ofPopMatrix();


       
}

